def generate_orders(db_path: str, count: int = 150) -> None:
    """Генерирует случайные заказы для таблицы Orders."""
    
    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()
    
    try:
        # Проверка, есть ли уже данные
        cursor.execute("SELECT COUNT(*) FROM Orders")
        result = cursor.fetchone()
        if result and result[0] >= 100:
            print(f"✓ В таблице Orders уже есть {result[0]} заказов")
            return
        
        # Получаем ID существующих товаров
        cursor.execute("SELECT ID FROM Products")
        product_ids = [row[0] for row in cursor.fetchall()]
        
        if not product_ids:
            print("⚠ В таблице Products нет товаров! Сначала выполните генерацию товаров.")
            return
        
        # Генерация дат за последний год
        end_date = datetime.now()
        start_date = end_date - timedelta(days=365)
        
        orders = []
        for _ in range(count):
            product_id = random.choice(product_ids)
            quantity = random.randint(1, 10)
            order_date = start_date + timedelta(days=random.randint(0, 365))
            orders.append((product_id, quantity, order_date.strftime('%Y-%m-%d')))
        
        # Вставка заказов
        cursor.executemany("""
            INSERT INTO Orders (Product_ID, Quantity, Order_Date)
            VALUES (?, ?, ?)
        """, orders)
        
        conn.commit()
        print(f"✓ Вставлено {count} заказов в таблицу Orders")
        
    except sqlite3.Error as e:
        print(f"⚠ Ошибка при вставке заказов: {e}")
        conn.rollback()
    finally:
        conn.close()


def print_statistics(db_path: str) -> None:
    """Выводит статистику по таблицам."""
    
    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()
    
    try:
        print("\n" + "=" * 60)
        print("📊 СТАТИСТИКА БАЗЫ ДАННЫХ")
        print("=" * 60)
        
        # Статистика Products
        cursor.execute("""
            SELECT 
                COUNT(*) AS total,
                ROUND(AVG(Price), 2) AS avg_price,
                MIN(Price) AS min_price,
                MAX(Price) AS max_price
            FROM Products
        """)
        row = cursor.fetchone()
        print(f"\n📦 Products:")
        print(f"  • Всего товаров: {row[0]}")
        print(f"  • Средняя цена: {row[1]} ₽")
        print(f"  • Диапазон цен: {row[2]} ₽ — {row[3]} ₽")
        
        # Статистика Orders
        cursor.execute("""
            SELECT 
                COUNT(*) AS total,
                SUM(Quantity) AS total_quantity,
                ROUND(AVG(Quantity), 2) AS avg_quantity
            FROM Orders
        """)
        row = cursor.fetchone()
        print(f"\n📋 Orders:")
        print(f"  • Всего заказов: {row[0]}")
        print(f"  • Общее количество: {row[1]} шт.")
        print(f"  • Среднее количество: {row[2]} шт.")
        
        # Статистика по категориям
        print(f"\n📈 Товары по категориям:")
        cursor.execute("""
            SELECT Category, COUNT(*) AS count
            FROM Products
            GROUP BY Category
            ORDER BY count DESC
        """)
        for row in cursor.fetchall():
            print(f"  • {row[0]:<25} | {row[1]}")
        
        print("\n" + "=" * 60)
        
    except sqlite3.Error as e:
        print(f"⚠ Ошибка при получении статистики: {e}")
    finally:
        conn.close()


